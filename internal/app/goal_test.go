package app

import "testing"

func TestExtractGoal(t *testing.T) {
	cases := []struct {
		query string
		want  float64
	}{
		{"план 2 миллиона", 2_000_000},
		{"план 2 млн", 2_000_000},
		{"цель 500 тысяч", 500_000},
		{"цель 500 тыс", 500_000},
		{"выйти на 300к", 300_000},
		{"дойти до 1.5 млн", 1_500_000},
		{"план 200 000", 200_000},
		{"план в 1 миллион", 1_000_000},
		// падежи «план»: плана/плану — основные фейлы batch-лога [30][31]
		{"дойду ли я до плана в 2 миллиона", 2_000_000},
		{"дойдём ли до плана в 1.5 млн", 1_500_000},
		{"дойти до плана в 800 тысяч", 800_000},
		// форма дойд- (дойду/дойдём) перед числом без «план»
		{"дойду до 800 тысяч", 800_000},
		{"дойдём до 500к", 500_000},
		// цель не названа — возвращаем 0
		{"дойду ли я до плана к концу месяца", 0},
		{"прогноз выручки на июнь", 0},
		{"если ничего не менять", 0},
	}
	for _, c := range cases {
		got := extractGoal(c.query)
		if got != c.want {
			t.Errorf("%q: got %v, want %v", c.query, got, c.want)
		}
	}
}
