package llm

// HasNonRussian грубо детектит срыв модели в чужой алфавит (qwen иногда вставляет
// КИТАЙСКИЙ в середине ответа, см. roadmap 5.5 — баг встречался и у нарратора, и у
// консультанта): если в ответе заметная доля букв вне базовой латиницы/кириллицы —
// текст бракуем, чтобы вызвавший упал на детерминированный Compose.
//
// Считаем буквами кириллицу, латиницу и «чужие» алфавитные руны (вне ASCII-латиницы/
// кириллицы и не пунктуация/символ); валюта/тире/проценты и т.п. не учитываются.
func HasNonRussian(s string) bool {
	var foreign, letters int
	for _, r := range s {
		switch {
		case r >= 'а' && r <= 'я', r >= 'А' && r <= 'Я', r == 'ё', r == 'Ё',
			r >= 'a' && r <= 'z', r >= 'A' && r <= 'Z':
			letters++
		case r > 0x2000 && !isPunctOrSymbol(r):
			// За пределами базовой латиницы/кириллицы и не пунктуация/символ — чужой алфавит.
			foreign++
			letters++
		}
	}
	if letters == 0 {
		return false
	}
	return foreign*100/letters > 10 // >10% чужих букв → брак
}

func isPunctOrSymbol(r rune) bool {
	// Валюта/тире/типографские кавычки и т.п. — не считаем чужим алфавитом.
	switch r {
	case '₽', '—', '–', '…', '«', '»', '“', '”', '’', '№', '%':
		return true
	}
	return false
}
