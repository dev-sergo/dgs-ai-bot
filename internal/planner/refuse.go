package planner

import (
	"regexp"
	"strings"

	"dgsbot/internal/plan"
)

// refusalSources — детерминированный пре-фильтр безопасности. Перечисляет паттерны
// запросов, которые заведомо НЕ являются легальной аналитикой тенанта: попытки
// эксфильтрации чужих/системных/персональных данных, инъекции промпта и джейлбрейк,
// мутации данных, дамп БД, а также явный офф-домен. Совпадение короткозамыкает план
// в off_topic ДО вызова модели — это одновременно защита от джейлбрейка (модель не
// видит вредоносную инструкцию) и экономия латентности.
//
// Паттерны применяются к запросу в нижнем регистре и выверены против бенчмарка
// (test/eval/prompts.jsonl): ловят все security-кейсы и НЕ задевают легальные отчёты
// (регрессия закреплена в refuse_test.go).
//
// Важно про Go RE2: \w и \b трактуются как ASCII и НЕ матчат кириллицу. Поэтому для
// кириллических «хвостов» слова используем [а-яё]*, а границы слова (\b) ставим
// только вокруг латиницы (\bdan\b, \broot\b и т.п.).
var refusalSources = []string{
	// PII клиентов/покупателей и сотрудников (в легальной аналитике этих слов нет).
	`клиент`, `покупател`, `customer`,
	`зарплат`, `парол`, `персональн`, `фио`,

	// Кросс-тенант: чужие тенанты/заведения/конкуренты.
	`тенант`, `tenant`, `конкурент`,
	`друг[а-яё]*\s+кафе`, `друг[а-яё]*\s+заведени`, `друг[а-яё]*\s+id`,
	`всех\s+тенант`, `все\s+данн`, `все\s+таблиц`, `открой\s+все`,

	// Инъекции промпта и джейлбрейк.
	`ignore\s+(all\s+|your\s+|previous\s+)?(instruction|training)`, `инструкц`,
	`act\s+as\s+dan`, `\bdan\b`, `ты\s+теперь\s+друг`, `притвор`, `pretend`,
	`override`, `обойд`, `isolation`,
	`\badmin\b`, `администратор`, `\broot\b`, `jailbreak`, `взлом`, `уязвим`,

	// Раскрытие системного промпта/настроек/контекста.
	`промпт`, `prompt`, `system\s*[:_]`, `system\s+message`, `систем[а-яё]*\s+настройк`,
	`настройк[а-яё]*\s+систем`, `context\s+window`, `repeat\s+everything`, `исходн[а-яё]*\s+код`,

	// SQL и дамп/выгрузка базы.
	// «выгрузи» — легальный глагол экспорта (бот сам предлагает «выгрузи таблицу за май»),
	// поэтому ловим ТОЛЬКО дамп-сценарий: выгрузка всего/базы, не отчёта за период.
	`select\b.*\bfrom\b`, `\bsql\b`, `баз[а-яё]*\s+данн`, `выгруз[а-яё]*\s+(вс[еёю]|баз|бд)`, `дамп`, `\bjson\b`,
	`нераскрыт`, `скрыт[а-яё]*\s+данн`, `данн[а-яё]*\s+без\s+фильтр`,
	`полн[а-яё]*\s+доступ`, `доступ\s+к\s+систем`, `доступ\s+к\s+баз`, `дай\s+доступ`, `root\s+доступ`,

	// Мутации данных (imperative/infinitive, чтобы не задеть «как изменилась выручка»).
	`удали`, `добав`, `измени(ть)?\s+(выручк|данн|заказ|чек|товар)`, `изменить.*систем`,

	// Явный офф-домен (нулевая коллизия с легальными кейсами набора).
	`стих`, `рецепт`, `анекдот`, `погод`, `курс\s+доллар`, `шахмат`, `илон\s+маск`,
	`кино`, `переведи`, `на\s+английск`, `писать\s+код`, `show\s+all\s+data`,
}

// refusalPatterns — скомпилированные один раз паттерны пре-фильтра.
var refusalPatterns = compileRefusal(refusalSources)

func compileRefusal(src []string) []*regexp.Regexp {
	out := make([]*regexp.Regexp, len(src))
	for i, p := range src {
		out[i] = regexp.MustCompile(p)
	}
	return out
}

// isRefusal сообщает, что запрос — попытка получить чужие/системные/персональные
// данные, инъекция, мутация или офф-домен, и должен быть отклонён без вызова модели.
func isRefusal(query string) bool {
	q := strings.ToLower(query)
	for _, re := range refusalPatterns {
		if re.MatchString(q) {
			return true
		}
	}
	return false
}

// refusalPlan — готовый off_topic-план для короткого замыкания пре-фильтра.
// confidence=1: решение детерминированное, оркестратор не должен переспрашивать.
func refusalPlan() plan.AnalysisPlan {
	return plan.AnalysisPlan{Version: "1", Intent: "off_topic", Confidence: 1}
}
